def count_letters(text):
    """
    Функция для подсчета букв в тексте (без учета регистра).

    :param text: Входной текст
    :return: Словарь с буквами нижнего регистра и их количеством
    """
    letter_count = {}

    for char in text:
        if char.isalpha():  # Проверяем, является ли символ буквой
            lower_char = char.lower()  # Приводим к нижнему регистру
            if lower_char in letter_count:
                letter_count[lower_char] += 1
            else:
                letter_count[lower_char] = 1

    return letter_count


def calculate_frequency(letter_count):
    """
    Функция для вычисления частоты каждой буквы.

    :param letter_count: Словарь с буквами и их количеством
    :return: Новый словарь с буквами и их частотой
    """
    total_letters = sum(letter_count.values())  # Общее количество букв
    frequency = {}

    for letter, count in letter_count.items():
        frequency[letter] = round(count / total_letters, 2)  # Вычисляем частоту и округляем до 2 знаков

    return frequency


# Пример использования
text = "Пример текста для подсчета частоты букв."
letter_count = count_letters(text)
frequency = calculate_frequency(letter_count)




